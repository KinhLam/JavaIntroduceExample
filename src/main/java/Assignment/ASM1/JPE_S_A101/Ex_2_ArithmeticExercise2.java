package Assignment.ASM1.JPE_S_A101;

public class Ex_2_ArithmeticExercise2 {
    public static void main(String[] args) {
        int a = 125;
        int b = 24;

        System.out.println("125 + 24 = " + (a + b));
        System.out.println("125 - 24 = " + (a - b));
        System.out.println("125 * 24 = " + (a * b));
        System.out.println("125 / 24 = " + (a / b));
        System.out.println("125 % 24 = " + (a % b));
    }
}
