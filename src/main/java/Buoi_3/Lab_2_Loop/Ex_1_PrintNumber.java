/*Exercise 1: Print Numbers
Write a program that uses a for loop to print numbers from 1 to 10 on the console*/

package Buoi_3.Lab_2_Loop;

public class Ex_1_PrintNumber {
    public static void main(String[] args){
        for(int i = 1; i <= 10; i++){
            System.out.println(i);
        }
    }
}
